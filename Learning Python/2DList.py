# 2D Lists = a list of lists

Breakfast = ["Platano", "Salami", "Huevo Frito"]
Lunch = ["Arroz", "Pollo", "Abichuela"]
Dinner = ["Mondongo", "Asopao", "Tinga"]

food = [Breakfast, Lunch, Dinner]

print(food[0][0])
print(food[0][1])
print(food[0][2])
print(food[1][0])
print(food[1][1])
print(food[1][2])
print(food[2][0])
print(food[2][1])
print(food[2][2])
